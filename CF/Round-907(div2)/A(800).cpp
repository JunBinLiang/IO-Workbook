void solve() {
  int n;
  cin >> n;
  vector<int> a(n);
  for(int i = 0; i < n; i++) {
    cin >> a[i];
  }

  set<int> s = {1, 2, 4, 8, 16};
  string ans = "YES";
  for(int i = 1; i < n; i++) {
    if(s.find(i) != s.end()) continue;
    if(a[i] < a[i - 1]) {
      ans = "NO";
    }
  }
  cout << ans << endl;
}

int main(){
  int t = 1;
  cin >> t;
  while(t--) {
    solve();
  }
  return 0;
}
